import express from 'express'
import mongoose from 'mongoose' 
import dotenv from 'dotenv'
import roleRoute from './routes/role.js'
import productRoute from './routes/product.js'
import categoryRoute from './routes/category.js'
import authRoute from './routes/auth.js'
import userRoute from './routes/user.js'
import buyRoute from './routes/buy.js'
import ratingRoute from "./routes/rating.js"
import cookieParser from 'cookie-parser'
import cors from 'cors'
import providerRoute from './routes/provider.js'
//import subirImg from './routes/subirIMG.js'

const app = express() 
dotenv.config()  //config dotenv paral as variables de entorno

//MIDDLEWARE
app.use(express.json())  //esto sirve para que en las peticiones json se acepte el body de las request
app.use(cookieParser())
app.use(cors({ origin: 'http://localhost:4200', credentials: true }))


app.use("/api/role", roleRoute)
app.use("/api/category", categoryRoute)
app.use("/api/product", productRoute)
app.use("/api/auth", authRoute)
app.use("/api/user", userRoute)
app.use("/api/buy", buyRoute)
app.use("/api/rating", ratingRoute)
app.use("/api/provider", providerRoute)
//app.use("/api/uploadimg", subirImg)

// RESPONSE HANDLER MIDDLEWARE
app.use((obj, req, res, next)=>{
    const statusCode = obj.status || 500
    const message = obj.message || "Something went wrong"
    return res.status(statusCode).json({
        success: [200,201,204].some(a=> a === obj.status) ? true: false, // si el obj manda un status del array es true
        status: statusCode,
        message: message, 
        data: obj.data
    })
}) 

// DB CONNECTION
const connectMongoDB = async ()=>{
    
    try{
        await mongoose.connect(process.env.MONGO_URL)  // .env
        console.log("Connected to Database")
    }catch(err){
        console.log("Error "+err.mongoose) 
    }
}


app.listen(8800, () =>{
    connectMongoDB()
    console.log("Connected to Backend")
})


